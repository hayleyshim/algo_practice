#문자열 인덱스 사용 및 슬라이싱
s1 = "Hello "
print("s1 = ", s1)

s2 = s1[3]
print("s2=", s2)

s3 = s1[2:7]
print(s3)
print("s3 = ", s3)

#문자열과 문자열 변수를 더할 수 있다.
s4 = "YOU"
s5 = "Global Reader " + s4

print("s4 = ", s4)
print("s5 = ", s5)

#문자열과 문자열을 더할 수 있다.
s6 = "Global Reader" + "YOU"
print("s6 = ", s6)

#숫자를 문자여로가 어하기 위해 변환한다
s7 = "YOU" + str(1) + "위"
print("s7 = ", s7)

s2 = "Good night"
print("s2 = ", s2)
