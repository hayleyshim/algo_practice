s1 = "Hello"
s2 = "010-8790-1234"

print("s1 = ", s1)
print("s1.count('l') = ", s1.count('l'))

print("s1.find('L') = ", s1.find('L'))
print("s1.find('L', 3) = ", s1.find('L', 3))

print("s1.index('2') = ", s2.index('2'))

